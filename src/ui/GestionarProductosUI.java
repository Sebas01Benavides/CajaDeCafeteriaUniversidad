    private void btnActualizarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarProductoActionPerformed
        if (productoSeleccionadoId == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un producto de la tabla para actualizar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        String nuevoNombre = txtNombreProducto.getText();
        double nuevoPrecio = Double.parseDouble(txtPrecio.getText());
        int nuevoStock = Integer.parseInt(txtStock.getText()); // Obtener el nuevo stock desde el campo txtStock

        // Validaciones b√°sicas
        if (nuevoNombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre del producto no puede estar vac√≠o.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (nuevoPrecio <= 0) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un n√∫mero positivo.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (nuevoStock < 0) { // Validaci√≥n para el stock
            JOptionPane.showMessageDialog(this, "El stock no puede ser negativo.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Para actualizar, obtenemos el estado 'activo' actual de la tabla (columna 3, √≠ndice 3)
        // Convertimos el String "Activo" o "Inactivo" a boolean
        boolean activoActual = ((String) tblProductos.getValueAt(tblProductos.getSelectedRow(), 3)).equals("Activo");
        
        // Crea un objeto Producto con los datos actualizados.
        // Se usa el constructor completo que incluye id, nombre, precio, activo, creado y stock.
        // Para 'creado', se puede usar LocalDateTime.now() o recuperar el valor original si lo hubieras almacenado.
        // Aqu√≠ asumimos que 'creado' no se actualiza en esta UI, as√≠ que se le pasa un valor v√°lido para el constructor.
        Producto productoActualizado = new Producto(
            productoSeleccionadoId, 
            nuevoNombre, 
            nuevoPrecio, 
            activoActual, 
            LocalDateTime.now(), // Pasa LocalDateTime.now() o la fecha original si la recuperas.
            nuevoStock
        ); 
        
        productoServicio.actualizarProducto(productoActualizado);
        
        JOptionPane.showMessageDialog(this, "Producto actualizado con √©xito.");
        cargarTablaProductos(); // Recarga la tabla para reflejar los cambios
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El precio y el stock deben ser n√∫meros v√°lidos.", "Error de formato", JOptionPane.ERROR_MESSAGE);
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Error de validaci√≥n: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al actualizar producto: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnActualizarProductoActionPerformed

    private void btnActivarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActivarProductoActionPerformed
        try {
            String nombre = txtNombreProducto.getText();
            double precio = Double.parseDouble(txtPrecio.getText());
            int stock = Integer.parseInt(txtStock.getText()); // Obtener el stock

            // Validaciones b√°sicas
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre del producto no puede estar vac√≠o.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (precio <= 0) {
                JOptionPane.showMessageDialog(this, "El precio debe ser un n√∫mero positivo.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
                return;
            }
             if (stock < 0) {
                JOptionPane.showMessageDialog(this, "El stock no puede ser negativo.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Crea un nuevo objeto Producto (activo por defecto, fecha de creaci√≥n actual)
            Producto nuevoProducto = new Producto(nombre, precio, stock);
            productoServicio.crearProducto(nuevoProducto);
            
            JOptionPane.showMessageDialog(this, "Producto agregado con √©xito.");
            cargarTablaProductos(); // Recarga la tabla para reflejar los cambios
            limpiarCampos();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio y el stock deben ser n√∫meros v√°lidos.", "Error de formato", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Error de validaci√≥n: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar producto: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnActivarProductoActionPerformed

    private void btnDesactivarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesactivarProductoActionPerformed
    int filaSeleccionada = tblProductos.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un producto de la tabla para desactivar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int idProducto = (int) tblProductos.getValueAt(filaSeleccionada, 0);
            productoServicio.cambiarEstado(idProducto, false);
            
            JOptionPane.showMessageDialog(this, "Producto desactivado con √©xito.");
            cargarTablaProductos();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al desactivar producto: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDesactivarProductoActionPerformed

