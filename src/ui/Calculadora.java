    private void equalsButtonActionPerformed(java.awt.event.ActionEvent evt) {
    if (!currentOperator.isEmpty()) {
        performCalculation(); 
        currentOperator = ""; // Resetea el operadordespués de la operación
    }
    startNewNumber = true; //Despues de dar un resultado se limpia memoria
    decimalAdded = false; 
}
    // MC Memory Clear Elimina cualquier número almacenado en memoria.
private void memoryClear() {
    memory = 0;
}

// MR (Memory Recall): Recupera el número almacenado en memoria y lo muestra. El número permanece en memoria.
private void memoryRecall() {
    jLabel1.setText(formatResult(memory)); // Muestra el número de la memoria en la pantalla
    startNewNumber = true; // El próximo número ingresado comenzará de nuevo
    decimalAdded = (memory % 1 != 0); // Ajusta la bandera decimal si el número de memoria tiene decimales
}
// MS (Memory Storage): Almacena en memoria el número mostrado.
private void memoryStore() {
    memory = Double.parseDouble(jLabel1.getText()); // Almacena el número actual en la pantalla
}
// M+: Suma el número mostrado a otro número que se encuentre en memoria.
private void memoryAdd() {
    memory += Double.parseDouble(jLabel1.getText());

}
// M-: Resta el número mostrado a otro número que se encuentre en memoria.
private void memorySubtract() {
    memory -= Double.parseDouble(jLabel1.getText());
}
//CE clear Entry Elimina el numero  mostrado
// Se utiliza para cuando se comete un error en el ingreso de datos pero sin eliminar el calculo
private void clearEntry() {
    jLabel1.setText("0"); // Limpia el numero actual en pantalla
    startNewNumber = true; //
    decimalAdded = false; // Reinicia decimal
}      
