    private void backspaceButtonActionPerformed(java.awt.event.ActionEvent evt) {
      String currentDisplay = jLabel1.getText();
        if (currentDisplay.length() > 1) {
            // Si hay mas de un caracter elimina el ultimo
            jLabel1.setText(currentDisplay.substring(0, currentDisplay.length() - 1));
        } else {
            jLabel1.setText("0");
            startNewNumber = true; 
        }
    }
