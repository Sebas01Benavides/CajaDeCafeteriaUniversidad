    private String formatResult(double result) {
        if (result == (long) result) {
            return String.format("%d", (long) result);
        } else {
            return String.format("%s", result);
    }
}
    private void numberButtonActionPerformed(java.awt.event.ActionEvent evt) {
        String buttonText = ((javax.swing.JButton)evt.getSource()).getText();
        String currentDisplay = jLabel1.getText();
        // Muestra numero en el label
        if (startNewNumber || currentDisplay.equals("0")) {
            jLabel1.setText(buttonText);
            startNewNumber = false; 
        } else {
            // Si ya hay numeros añade el nuevo numero al final
            // Limita la cantidad de caracteres en el label a 10
            if (currentDisplay.length() < 10) { // Limite de 10
                jLabel1.setText(currentDisplay + buttonText);
            }
        }
    }
//
private void performCalculation() {
    double secondNumber = Double.parseDouble(jLabel1.getText());
    double result = 0;
    switch (currentOperator) {
        case "+":
            result = firstNumber + secondNumber;
            break;
        case "-":
            result = firstNumber - secondNumber;
            break;
        case "*":
            result = firstNumber * secondNumber;
            break;
        case "/":
            if (secondNumber != 0) {
                result = firstNumber / secondNumber;
            } else {
                jLabel1.setText("Error");
                firstNumber = 0;
                currentOperator = "";
                startNewNumber = true;
                decimalAdded = false;
                return;
            }
            break;
        case "MOD":
            if (secondNumber != 0) {
                result = firstNumber % secondNumber;
            } else {
                jLabel1.setText("Error");
                firstNumber = 0;
                currentOperator = "";
                startNewNumber = true;
                decimalAdded = false;
                return;
            }
            break;
    }
//Muestra resultado en el label
    jLabel1.setText(formatResult(result));
    firstNumber = result;
}
